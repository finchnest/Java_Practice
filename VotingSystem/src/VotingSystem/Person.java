package VotingSystem;

public class Person {
    
    private String fname;
    private int age;

    public String getFname() {
        return fname;
    }

    public int getAge() {
        return age;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
